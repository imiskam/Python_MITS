# Задание 2
# Дан список некоторых целых чисел, найдите значение 20 в нем и,
# если оно присутствует, замените его на 200. Обновите список
# только при первом вхождении числа 20.

def replace20_200(list_):
    for i in list_:
        if i == 20:
            list_[list_.index(i)] = 200
            break  # остановка после первого вхождения
    print(list_)


replace20_200([7, 19, 12, 34, 20, 20, 35, 27])


