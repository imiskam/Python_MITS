# Задание 1
# Дан произвольный список. Представьте его в обратном порядке.

def reverse_list(list_):
    # 1 вариант
    print(list_[::-1])
    # 2 вариант
    print(reversed(list_))
    # 3 вариант
    list_.reverse()
    print(list_)


reverse_list([1, 2, 3, "a", "b", "c"])


