# Задание 4
# Найти совпадающие элементы двух списков.
# a = [5,[1,2],2,'r',4,'ee']
# b = [4,'we','ee',3,[1,2]]
# Эти значения записать в новый список

def find_duplicates(list1, list2):
    a = list1
    b = list2
    # 1 вариант
    duplicates_list = []
    for i in a:
        if i in b:
            duplicates_list.append(i)
    print(duplicates_list)
    # 2 вариант c генератором
    print([i for i in a if i in b])


find_duplicates([5, [1, 2], 2, "r", 4, "ee"], [4, "we", "ee", 3, [1, 2]])


