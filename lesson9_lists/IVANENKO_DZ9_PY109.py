# Задание 5
# Даны 2 списка:
# a = [4,6,'pу','tell',78]
# b = [44,'hello’,56,'exept’,3]
# Выполнить следующие операции:
# 1. Сложить два списка
# 2. Добавьте элемент 6 на 3 позицию.
# 3. Удалите все текстовые переменные
# 4. Посчитайте количество элементов списка

def func_task5(list1, list2):
    a = list1
    b = list2
    # 1
    full_list = a + b
    print("Объединенный список:", full_list)
    # 2
    full_list.insert(2, 6)
    print("Новый элемент в списке:", full_list)
    # 3
    for i in full_list:
        if type(i) is str:
            full_list.remove(i)
    print("Список без текстовых переменных:", full_list)
    # 4
    print("Количество элементов списка:", len(full_list))


func_task5([4, 6, 'pу', 'tell', 78], [44, 'hello', 56, 'exept', 3])

