# Задание 3
# Cписок из 7 цифр. Если четных цифр в нем больше чем нечетных,
# то найти сумму всех его цифр, если нечетных больше, то найти
# произведение 1 3 и 6 элемента.

def func_task3(list_):
    even = 0
    odd = 0
    sum_ = 0
    for i in list_:
        sum_ += i
        if i % 2 == 0:
            even += 1
        else:
            odd += 1
    if even > odd:
        print(sum_)
    else:
        print(list_[0] * list_[2] * list_[5])


func_task3([7, 19, 12, 34, 20, 20, 35, 27])


