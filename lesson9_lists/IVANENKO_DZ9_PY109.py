# Домашнее задание
# Дан список list=[15,48,'hello',6,19,'world’].
# Все числа этого списка проверить на чётность. Если число
# чётное, то посчитать сумму его цифр. Если нечётное, то
# заменить его на 1 в списке.
# Все слова: посчитать количество гласных и согласных.
# Вывести всё на экран.


def list_operations(list_):
    sum_ = 0
    vowels = "aeiouAEIOUауоыэяюёиеАУОЫЭЯЮЁИЕ"
    vowels_count = 0
    consonants_count = 0

    for elem in list_:
        # логика для чисел
        if type(elem) is int:  # если тип объекта - число
            if elem % 2 == 0:  # также если число делится на 2 без остатка
                while elem in list_:  # заходим в цикл и определяем переменную для цифр digit
                    digit = elem % 10  # вычисляем остаток от числа (т.е. его последнюю цифру каким бы число не было)
                    sum_ += digit  # к итоговой сумме прибавляем полученную цифру
                    elem = elem // 10  # вычисляем первую цифру числа через целочисленное деление
            else:
                odd_elem = list_.index(elem)  # находим индекс нечетного элемента в списке
                list_[odd_elem] = 1  # при нахождении нечетного числа значение индекса заменяется на 1
        # логика для букв
        elif type(elem) is str:  # если тип объекта - строка
            for letter in elem:  # для определенной буквы в строковом объекте списка
                if letter in vowels:  # если такая буква является гласной (в переменной vowels)
                    vowels_count += 1  # прибавляем +1 к гласным
                else:
                    consonants_count += 1  # прибавляем +1 к согласным

    print(f"Сумма цифр списка: {sum_}")
    print(f"Список с замененными числами: {list_}")
    print(f"Количество гласных в списке: {vowels_count}, согласных: {consonants_count}")


list_operations([15, 48, 'hello', 6, 19, 'world'])
