# переменные
int1 = int(input("Введите первое число: "))
int2 = int(input("Введите второе число: "))
int3 = int(input("Введите третье число: "))

# прибавление
print(int1 + int2 + int3)

# вычитание
print(int1 - int2 - int3)

# деление
print(int1 / int2 / int3)

# степень
print(int1 ** int2 ** int3)
